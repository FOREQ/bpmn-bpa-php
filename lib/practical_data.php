function randomPracticalTaskIds(): array
{
    $firstTasks = getFirstPracticalTasks();
    $secondTasks = getPracticalTasks();

    $firstTask = $firstTasks[array_rand($firstTasks)];
    $secondTask = $secondTasks[array_rand($secondTasks)];

    return [
        $firstTask['id'],
        $secondTask['id']
    ];
}

function randomComplexityVariantId(): string
{
    $variants = getComplexityVariants();
    $variant = $variants[array_rand($variants)];

    return $variant['id'];
}

function getPracticalTask(string $id): ?array
{
    $allTasks = array_merge(getFirstPracticalTasks(), getPracticalTasks());

    foreach ($allTasks as $task) {
        if ($task['id'] === $id) {
            return $task;
        }
    }

    return null;
}

function isFirstPracticalTaskId(string $id): bool
{
    foreach (getFirstPracticalTasks() as $task) {
        if ($task['id'] === $id) {
            return true;
        }
    }

    return false;
}

function getComplexityVariant(string $id): ?array
{
    foreach (getComplexityVariants() as $variant) {
        if ($variant['id'] === $id) {
            return $variant;
        }
    }

    return null;
}